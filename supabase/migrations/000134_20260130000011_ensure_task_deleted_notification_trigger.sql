-- ============================================================================
-- Task 삭제 알림 트리거 확인 및 재생성
-- ============================================================================
-- 목적: Task 삭제 시 담당자에게 알림이 정상적으로 생성되도록 트리거 확인 및 재생성
-- 
-- 요구사항:
-- - Task 삭제 시 새로운 알림 row가 생성되어야 함
-- - 알림 count가 증가해야 함
-- - 기존 알림을 덮는 게 아니라 새로운 알림이 추가되어야 함
-- ============================================================================

BEGIN;

-- ----------------------------------------------------------------------------
-- 1. 트리거가 존재하는지 확인하고 재생성
-- ----------------------------------------------------------------------------

-- 기존 트리거 제거 (있는 경우)
DROP TRIGGER IF EXISTS trigger_create_task_deleted_notification ON public.tasks;

-- 트리거 재생성 (함수가 이미 최신 버전으로 업데이트되어 있음)
CREATE TRIGGER trigger_create_task_deleted_notification
  AFTER DELETE ON public.tasks
  FOR EACH ROW
  EXECUTE FUNCTION public.create_task_deleted_notification();

-- ----------------------------------------------------------------------------
-- 2. 트리거 코멘트 추가
-- ----------------------------------------------------------------------------

COMMENT ON TRIGGER trigger_create_task_deleted_notification ON public.tasks IS 
'Task 삭제 시 담당자에게 TASK_DELETED 알림을 생성하는 트리거. Task 삭제 시 새로운 알림 row가 생성되고 알림 count가 증가합니다.';

COMMIT;
