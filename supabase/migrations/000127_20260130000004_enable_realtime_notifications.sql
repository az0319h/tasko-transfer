-- ============================================================================
-- 알림 테이블 Realtime 활성화
-- ============================================================================
-- 목적: notifications 테이블에 Realtime 활성화하여 실시간 알림 수신 가능하도록 설정
-- 
-- 작업 내용:
-- 1. notifications 테이블을 Realtime publication에 추가
-- 2. 사용자별 필터링은 프론트엔드에서 처리
-- ============================================================================

BEGIN;

-- ----------------------------------------------------------------------------
-- 1. Realtime publication에 notifications 테이블 추가
-- ----------------------------------------------------------------------------

-- Realtime publication에 notifications 테이블 추가
ALTER PUBLICATION supabase_realtime ADD TABLE public.notifications;

-- ----------------------------------------------------------------------------
-- 2. 주석 추가
-- ----------------------------------------------------------------------------

COMMENT ON TABLE public.notifications IS 
'알림 테이블: Realtime 활성화됨. 사용자별 필터링은 프론트엔드에서 user_id=eq.{currentUserId} 필터 사용';

COMMIT;
